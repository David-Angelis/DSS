    private void ProcurarEquipa_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ProcurarEquipa_ButtonActionPerformed
        try {
            Equipa equipa = a.getEquipa(Escola_ComboBox1.getSelectedItem().toString(), Escola_ComboBox1.getSelectedItem().toString() + "-" + Equipa_ComboBox2.getSelectedItem().toString());
            if (equipa != null) {
                EquipaSelecionada_Label.setText(Equipa_ComboBox2.getSelectedItem().toString());
                Jogadores_List2.setModel(new DefaultListModel());
                DefaultListModel modelo = (DefaultListModel) Jogadores_List2.getModel();
                for (Jogador c : a.getDAOEscola().get(Escola_ComboBox1.getSelectedItem().toString()).getJogadores(Escola_ComboBox1.getSelectedItem().toString() + "-" + Equipa_ComboBox2.getSelectedItem().toString())) {
                    modelo.addElement(c.getNome() + "-" + c.getDataNascimento());
                }
            }
            for (Component component : DadosJogador_Panel.getComponents()) {
                component.setEnabled(true);
            }
            DadosJogador_Panel.setEnabled(true);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ProcurarEquipa_ButtonActionPerformed

    private void RemoverEquipa_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoverEquipa_ButtonActionPerformed
        try {
            if (EquipaSelecionada_Label.getText().equals("nenhuma")) {
                JOptionPane.showMessageDialog(null, "Não está nenhuma equipa seleccionada! Tem que seleccionar uma equipa!", "Alerta!", JOptionPane.WARNING_MESSAGE);
            } else {
                String equipaSelec = EquipaSelecionada_Label.getText();
                EquipaSelecionada_Label.setText("nenhuma");
                a.RemoveEquipa(Escola_ComboBox1.getSelectedItem().toString(), Escola_ComboBox1.getSelectedItem().toString() + "-" + equipaSelec);
                Equipa_ComboBox2.removeAllItems();
                Escola escola = a.getDAOEscola().get(Escola_ComboBox1.getSelectedItem().toString());
                if (escola != null) {
                    for (Equipa equipa : escola.getEquipas(escola)) {
                        Equipa_ComboBox2.addItem(equipa.getNome());
                    }
                }
                for (Component component : DadosJogador_Panel.getComponents()) {
                    component.setEnabled(false);
                }
                DadosJogador_Panel.setEnabled(false);
            }
        } catch (HeadlessException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro!", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_RemoverEquipa_ButtonActionPerformed

