    private void CriarEquipa_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CriarEquipa_ButtonActionPerformed
        // TODO add your handling code here:
        Jogadores_List.clearSelection();
        try {
            if (NomeEquipa_TextField.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Tem de preencher o nome da equipa!", "Erro!", JOptionPane.ERROR_MESSAGE);
            } else if (Escola_ComboBox.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(null, "Tem de selecionar uma escola!", "Erro!", JOptionPane.ERROR_MESSAGE);
            } else if (Escalao_ComboBox.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(null, "Tem de selecionar um escalão!", "Erro!", JOptionPane.ERROR_MESSAGE);
            } else {
                a.criaEquipa(Escola_ComboBox.getSelectedItem().toString(), Escola_ComboBox.getSelectedItem().toString() + "-" + NomeEquipa_TextField.getText(), NomeEquipa_TextField.getText(), "Escalao." + Escalao_ComboBox.getSelectedItem().toString());

                //Atualiza combobox equipa na tab gerir equipa
                Equipa_ComboBox2.removeAllItems();
                Escola escola = a.getDAOEscola().get(Escola_ComboBox1.getSelectedItem().toString());
                if (escola != null) {
                    for (Equipa equipa : escola.getEquipas(escola)) {
                        Equipa_ComboBox2.addItem(equipa.getNome());
                    }
                }

                JOptionPane.showMessageDialog(null, "Equipa criada com sucesso!", "Sucesso!", JOptionPane.DEFAULT_OPTION);
               
                AdicionarJogador_Panel.setEnabled(true);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro!", JOptionPane.ERROR_MESSAGE);
        }
         for (Component component : AdicionarJogador_Panel.getComponents()) {
                    component.setEnabled(true);
                }
    }//GEN-LAST:event_CriarEquipa_ButtonActionPerformed

