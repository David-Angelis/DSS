    private void RegistarResultado_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistarResultado_ButtonActionPerformed
        buttonGroup1.clearSelection();
        HashMap<String, Integer> jogadores_1 = new HashMap<String,Integer>();
        HashMap<String, Integer> jogadores_2 = new HashMap<String,Integer>();
        String jogador;
        for (int i=0; i < modelo1.getSize(); i++) {
            jogador = modelo1.getElementAt(i).toString();
            if (!jogador.equals("Auto Golo")) {
                if (jogadores_1.containsKey(jogador))
                    jogadores_1.put(jogador, (jogadores_1.get(jogador)+1));
                else
                    jogadores_1.put(jogador, 1);
            }
        }
        for (int i=0; i < modelo2.getSize(); i++) {
            jogador = modelo2.getElementAt(i).toString();
            if (!jogador.equals("Auto Golo")) {
                if (jogadores_2.containsKey(jogador))
                    jogadores_2.put(jogador, (jogadores_2.get(jogador)+1));
                else
                    jogadores_2.put(jogador, 1);
            }
        }
        String cod_competicao;
        if (Campeonato_RadioButton.isSelected()){
           cod_competicao = "Escalao." + Escalao_ComboBox.getSelectedItem().toString(); 
            try {
            a.SetGolo(cod_competicao, jogadores_1, jogadores_2);
            int Golos1 = Integer.parseInt(Golos1_Label.getText());
            int Golos2 = Integer.parseInt(Golos2_Label.getText());
            String[]cod= ResultadosPorRegistar_List.getSelectedValue().toString().split("-");
            String[] equipas = ResultadosPorRegistar_List.getSelectedValue().toString().split(" vs ");
            String cod_equipa1 = equipas[0];
            String cod_equipa2 = equipas[1];
            a.SetResultado(cod_competicao, cod_competicao+Jornada_ComboBox.getSelectedIndex(),cod_equipa1, cod_equipa2, Golos1, Golos2);
            JOptionPane.showMessageDialog(null, "Resultado registado com sucesso!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
        }
        catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro!", JOptionPane.ERROR_MESSAGE);
        }
        }
            
        else{
            cod_competicao = Competicao_ComboBox.getSelectedItem().toString();
             try {
            a.SetGolo(cod_competicao, jogadores_1, jogadores_2);
            int Golos1 = Integer.parseInt(Golos1_Label.getText());
            int Golos2 = Integer.parseInt(Golos2_Label.getText());
            String[]cod= ResultadosPorRegistar_List.getSelectedValue().toString().split("-");
            String[] equipas = ResultadosPorRegistar_List.getSelectedValue().toString().split(" vs ");
            String cod_equipa1 = equipas[0];
            String cod_equipa2 = equipas[1];
            a.SetResultado(cod_competicao,cod_competicao+"_"+(Jornada_ComboBox.getSelectedIndex()+1),cod_equipa1, cod_equipa2, Golos1, Golos2);
            JOptionPane.showMessageDialog(null, "Resultado registado com sucesso!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
        }
        catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro!", JOptionPane.ERROR_MESSAGE);
        }
        }
       modelo1.removeAllElements();
        modelo2.removeAllElements();
        atualiza_ResultadosPorRegistar_List();
    }//GEN-LAST:event_RegistarResultado_ButtonActionPerformed

