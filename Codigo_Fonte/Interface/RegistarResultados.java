    private void atualiza_Competicao_ComboBox() {
        Competicao_ComboBox.removeAllItems();
        String escalao;
        for (Competicao c: a.getDAOCompeticao().values()) {
            escalao = c.getEscalao().getClass().toString();
            if (escalao.equals("class Escalao." + Escalao_ComboBox.getSelectedItem().toString())) {
                if(Campeonato_RadioButton.isSelected() && c.getClass().getName().equals("Competicao.Campeonato")) {
                    Competicao_ComboBox.addItem(c.getNome());
                }
                if(Torneio_RadioButton.isSelected() && (c instanceof Torneio))
                    Competicao_ComboBox.addItem(c.getNome());
            }
        }
    }
    
    private void atualiza_ResultadosPorRegistar_List() {
        Competicao competicao;
        ResultadosPorRegistar_List.setModel(new DefaultListModel());
        DefaultListModel modelo = (DefaultListModel) ResultadosPorRegistar_List.getModel();
        
        if (Campeonato_RadioButton.isSelected()) {
            competicao = a.getDAOCompeticao().get("Escalao." + Escalao_ComboBox.getSelectedItem().toString());
            if(competicao != null) {
                String cod_jornada = competicao.getCod_competicao() + Jornada_ComboBox.getSelectedIndex();
                String cod_competicao = "Escalao." + Escalao_ComboBox.getSelectedItem().toString();
                Collection<Jogo> jogos = competicao.getJogos().
                        values(cod_competicao, cod_jornada);
                if(jogos != null) {
                    for(Jogo jogo: jogos) {
                        if (jogo.getGolo1()== -1 || jogo.getGolo2()== -1) {
                            String jogoString = jogo.getEquipa1() + " vs " + jogo.getEquipa2();
                            modelo.addElement(jogoString);
                        }
                    }
                }
            }
        }
        if (Torneio_RadioButton.isSelected()) {
            if(Competicao_ComboBox.getSelectedIndex()>-1){
                competicao = a.getDAOCompeticao().get(Competicao_ComboBox.getSelectedItem().toString());
                if(competicao != null) {
                    String cod_jornada = competicao.getCod_competicao()+"_"+(Jornada_ComboBox.getSelectedIndex()+1);
                    String cod_competicao = Competicao_ComboBox.getSelectedItem().toString();
                    Collection<Jogo> jogos = competicao.getJogos().
                            values(cod_competicao, cod_jornada);
                    if(jogos != null) {
                        for(Jogo jogo: jogos) {
                            if (jogo.getGolo1()== -1 || jogo.getGolo2()== -1) {
                                String jogoString =jogo.getEquipa1() + " vs " + jogo.getEquipa2();
                                modelo.addElement(jogoString);
                            }
                        }
                    }
                }
            }
        }
    }
    
