    private void CriarTorneio_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CriarTorneio_ButtonActionPerformed
        // TODO add your handling code here:
        Collection<String> equipas = new ArrayList<String>();
        DefaultListModel model1 = (DefaultListModel) jList4.getModel();
        for (int i = 0; i < model1.size(); i++) {
            equipas.add((String) jList4.getModel().getElementAt(i));
        }
        if ((equipas.size() & -equipas.size()) == equipas.size()&& equipas.size()!=0) {
            try {
                a.CriarCompeticao(NomeTorneio_TextField.getText(), "Escalao." + EscalaoTorneio_ComboBox.getSelectedItem().toString(), equipas, 1, TipoTorneio_ComboBox.getSelectedItem().toString(), jFormattedTextField3.getText());
                JOptionPane.showMessageDialog(null, "Torneio inserido com sucesso", "OK", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Numero de Equipas insuficiente", "ERROR", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_CriarTorneio_ButtonActionPerformed

