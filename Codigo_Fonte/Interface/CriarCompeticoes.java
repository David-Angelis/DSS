    private void CriarCampeonato_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CriarCampeonato_ButtonActionPerformed
        // TODO add your handling code here:
        Collection<String> equipas = new ArrayList<String>();
        DefaultListModel model1 = (DefaultListModel) jList5.getModel();
        for (int i = 0; i < model1.size(); i++) {
            equipas.add((String) jList5.getModel().getElementAt(i));
        }
        try {
            if (equipas.size()>=2 && equipas.size()%2==0) {
                a.CriarCompeticao(NomeCampeonato_TextField.getText(), "Escalao." + EscalaoCampeonato_ComboBox.getSelectedItem().toString(), equipas, 0, "", jFormattedTextField2.getText());
                JOptionPane.showMessageDialog(null, "Campeonato inserido com sucesso", "OK", JOptionPane.INFORMATION_MESSAGE);

            } else {
                JOptionPane.showMessageDialog(null, "Numero de Equipas insuficiente", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_CriarCampeonato_ButtonActionPerformed

